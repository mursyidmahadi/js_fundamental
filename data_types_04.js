// primatives

// string
const student = 'Sara'
console.log(student);
console.log(typeof student);

// Number
const age = 20;
console.log(age);
console.log(typeof age);

// boolean
const isMarried = true;
console.log(isMarried);
console.log(typeof isMarried);

// Null
const car = null;
console.log(car);
console.log(typeof car);

// undefined
let test;
console.log(test);
console.log(typeof test);

// symbol
const sym = Symbol();
console.log(sym);
console.log(typeof sym);

// Reference types (ALL OF IT IS OBJECT)

// array
const id = [1,2,3,4,5,6];
console.log(id);
console.log(typeof id);

// object literals
const passenger = {
    firstName : 'Sara',
    lastName : 'Smith',
    age:32
};

console.log(passenger);
console.log(typeof passenger);

// built-in object
const today = new Date();
console.log(today);
console.log(typeof today);